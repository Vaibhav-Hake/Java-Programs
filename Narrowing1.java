class Narrowing1{
	public static void main(String[]args){
		double a=97.11255565545;
		float b=(float)a;
		long c=(long)a;
		int d=(int)a;
		char e=(char)a;
		short f=(short)a;
		byte g=(byte)a;
		System.out.println(a);
	   System.out.println(b);
	   System.out.println(c);
	   System.out.println(d);
	   System.out.println(e);
	   System.out.println(f);
	   System.out.println(g);
	}
}