class Narrowing2{
	public static void main(String []args){
		float a=69.452f;
		long b=(long)a;
		int c=(int)a;
		char d=(char)a;
		short e=(short)a;
		byte f=(byte)a;
		System.out.println(a);
	 System.out.println(b);
	 System.out.println(c);
	 System.out.println(d);
	 System.out.println(e);
	 System.out.println(f);
	}
}