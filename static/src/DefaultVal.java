class DefaultVal{
	static int a;
	static byte b;
	static short c;
	static long d;
	static float e;
	static double f;
	static char ch;
	static boolean g;
	public static void main(String[]args){
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(ch);
		System.out.println(g);
	}
	
}